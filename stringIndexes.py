selfish = '01234567'
# 01234567
# [start:stop:stepover]
# called string slicing
# Immutability - Cannot be changed. For instance you cannot change the variable's value once it is created
print(selfish[0:8:2])
print(selfish[1:])
print(selfish[:5])
print(selfish[::1])
print(selfish[-1])
print(selfish[::-1])